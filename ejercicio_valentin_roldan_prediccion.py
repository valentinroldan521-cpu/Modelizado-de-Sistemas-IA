axes[1].grid(True, alpha=0.3)

# Residuos
residuals = y_test - y_pred_nn
axes[2].hist(residuals, bins=40, color='#16a085', edgecolor='white', alpha=0.85)
axes[2].axvline(0, color='red', linestyle='--', lw=2, label='Error = 0')
