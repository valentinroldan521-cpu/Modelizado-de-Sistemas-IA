if r2_nn > r2_r:
    print(f'\n✅ La red neuronal mejoró el R² en +{r2_nn - r2_r:.4f} ({(r2_nn - r2_r)/abs(r2_r)*100:.1f}%)')
else:
    print(f'\n⚠️ El modelo lineal superó a la red (diferencia: {r2_r - r2_nn:.4f}). Revisá la arquitectura.')

